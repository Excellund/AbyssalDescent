drop function if exists public.submit_leaderboard_run(jsonb);
create or replace function public.submit_leaderboard_run(
  p_run_summary jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_run_id text := lower(trim(coalesce(p_run_summary->>'run_id', '')));
  v_player_uuid text := lower(trim(coalesce(p_run_summary->>'player_uuid', '')));
  v_player_name text := trim(coalesce(p_run_summary->>'player_name', 'Player'));
  v_character_id text := lower(trim(coalesce(p_run_summary->>'character_id', 'unknown')));
  v_character_name text := trim(coalesce(p_run_summary->>'character_name', 'Unknown'));
  v_patch_key text := lower(trim(coalesce(p_run_summary->>'leaderboard_patch_key', 'dev')));
  v_outcome text := lower(trim(coalesce(p_run_summary->>'outcome', 'unknown')));
  v_duration_seconds int := greatest(0, coalesce((p_run_summary->>'duration_seconds')::int, 0));
  v_started_at_unix bigint := greatest(0, coalesce((p_run_summary->>'started_at_unix')::bigint, 0));
  v_ended_at_unix bigint := greatest(0, coalesce((p_run_summary->>'ended_at_unix')::bigint, 0));
  v_difficulty_tier int := coalesce((p_run_summary->>'difficulty_tier')::int, -1);
  v_is_debug boolean := coalesce((p_run_summary->>'is_debug')::boolean, false);
  v_clear_time_ms bigint;
begin
  if v_run_id = '' then
    return jsonb_build_object('ok', false, 'error', 'missing_run_id');
  end if;
  if v_player_uuid = '' then
    return jsonb_build_object('ok', false, 'error', 'missing_player_uuid');
  end if;
  if v_outcome <> 'clear' then
    return jsonb_build_object('ok', false, 'error', 'outcome_not_clear');
  end if;
  if v_is_debug then
    return jsonb_build_object('ok', false, 'error', 'debug_not_eligible');
  end if;
  if v_difficulty_tier < 0 or v_difficulty_tier > 3 then
    return jsonb_build_object('ok', false, 'error', 'invalid_difficulty_tier');
  end if;
  if v_duration_seconds <= 0 then
    return jsonb_build_object('ok', false, 'error', 'invalid_duration');
  end if;
  if v_started_at_unix <= 0 then
    return jsonb_build_object('ok', false, 'error', 'invalid_started_at');
  end if;
  if v_ended_at_unix < v_started_at_unix then
    return jsonb_build_object('ok', false, 'error', 'invalid_ended_at');
  end if;
  if v_player_name = '' then
    v_player_name := 'Player';
  end if;
  if v_patch_key = '' then
    v_patch_key := 'dev';
  end if;

  v_clear_time_ms := (v_duration_seconds::bigint * 1000);

  insert into public.leaderboard_runs (
    run_id,
    player_uuid,
    player_name,
    character_id,
    character_name,
    difficulty_tier,
    outcome,
    duration_seconds,
    clear_time_ms,
    leaderboard_patch_key,
    started_at_unix,
    ended_at_unix,
    is_debug,
    run_summary,
    updated_at
  ) values (
    v_run_id,
    v_player_uuid,
    v_player_name,
    v_character_id,
    v_character_name,
    v_difficulty_tier,
    'clear',
    v_duration_seconds,
    v_clear_time_ms,
    v_patch_key,
    v_started_at_unix,
    v_ended_at_unix,
    false,
    coalesce(p_run_summary, '{}'::jsonb),
    now()
  )
  on conflict (run_id) do update
  set
    player_uuid = excluded.player_uuid,
    player_name = excluded.player_name,
    character_id = excluded.character_id,
    character_name = excluded.character_name,
    difficulty_tier = excluded.difficulty_tier,
    duration_seconds = excluded.duration_seconds,
    clear_time_ms = excluded.clear_time_ms,
    leaderboard_patch_key = excluded.leaderboard_patch_key,
    started_at_unix = excluded.started_at_unix,
    ended_at_unix = excluded.ended_at_unix,
    is_debug = excluded.is_debug,
    run_summary = excluded.run_summary,
    updated_at = now();

  return jsonb_build_object('ok', true, 'run_id', v_run_id);
end;
$$;

revoke all on function public.submit_leaderboard_run(jsonb) from public;
grant execute on function public.submit_leaderboard_run(jsonb) to anon;
grant execute on function public.submit_leaderboard_run(jsonb) to authenticated;

drop function if exists public.get_leaderboard_top(text, integer, text, text, integer);
create or replace function public.get_leaderboard_top(
  p_patch_key text,
  p_difficulty_tier integer,
  p_board_mode text default 'global',
  p_character_id text default '',
  p_limit integer default 25
)
returns jsonb
language sql
security definer
set search_path = public
as $$
  with filtered as (
    select
      run_id,
      player_uuid,
      player_name,
      character_id,
      character_name,
      difficulty_tier,
      duration_seconds,
      leaderboard_patch_key,
      ended_at_unix
    from public.leaderboard_runs
    where is_debug = false
      and outcome = 'clear'
      and leaderboard_patch_key = lower(trim(p_patch_key))
      and difficulty_tier = p_difficulty_tier
      and (
        lower(trim(coalesce(p_board_mode, 'global'))) <> 'per_character'
        or character_id = lower(trim(coalesce(p_character_id, '')))
      )
  ),
  best_per_player as (
    select distinct on (player_uuid)
      run_id,
      player_uuid,
      player_name,
      character_id,
      character_name,
      difficulty_tier,
      duration_seconds,
      leaderboard_patch_key,
      ended_at_unix
    from filtered
    order by player_uuid, duration_seconds asc, ended_at_unix asc, run_id asc
  ),
  ranked as (
    select
      row_number() over(order by duration_seconds asc, ended_at_unix asc, run_id asc) as rank,
      run_id,
      player_uuid,
      player_name,
      character_id,
      character_name,
      difficulty_tier,
      duration_seconds,
      leaderboard_patch_key,
      ended_at_unix
    from best_per_player
    order by duration_seconds asc, ended_at_unix asc, run_id asc
    limit greatest(1, least(p_limit, 25))
  )
  select coalesce((select jsonb_agg(to_jsonb(ranked)) from ranked), '[]'::jsonb);
$$;

revoke all on function public.get_leaderboard_top(text, integer, text, text, integer) from public;
grant execute on function public.get_leaderboard_top(text, integer, text, text, integer) to anon;
grant execute on function public.get_leaderboard_top(text, integer, text, text, integer) to authenticated;

drop function if exists public.get_leaderboard_patch_keys(integer, integer, text);
create or replace function public.get_leaderboard_patch_keys(
  p_difficulty_tier integer default -1,
  p_limit integer default 24,
  p_current_patch_key text default ''
)
returns jsonb
language sql
security definer
set search_path = public
as $$
  with patches as (
    select distinct leaderboard_patch_key as patch_key
    from public.leaderboard_runs
    where is_debug = false
      and outcome = 'clear'
      and (
        p_difficulty_tier < 0
        or difficulty_tier = p_difficulty_tier
      )
  ),
  ordered as (
    select
      patch_key,
      patch_key = lower(trim(coalesce(p_current_patch_key, ''))) as is_current,
      patch_key <> lower(trim(coalesce(p_current_patch_key, ''))) as is_archived
    from patches
    order by patch_key desc
    limit greatest(1, least(p_limit, 40))
  )
  select coalesce((select jsonb_agg(to_jsonb(ordered)) from ordered), '[]'::jsonb);
$$;

revoke all on function public.get_leaderboard_patch_keys(integer, integer, text) from public;
grant execute on function public.get_leaderboard_patch_keys(integer, integer, text) to anon;
grant execute on function public.get_leaderboard_patch_keys(integer, integer, text) to authenticated;
