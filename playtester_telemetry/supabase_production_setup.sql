  on public.leaderboard_runs (player_uuid, leaderboard_patch_key, difficulty_tier, character_id, duration_seconds);

alter table public.leaderboard_runs enable row level security;

drop policy if exists leaderboard_runs_insert_anon on public.leaderboard_runs;
create policy leaderboard_runs_insert_anon
on public.leaderboard_runs
for insert
to anon
with check (
  length(trim(run_id)) >= 8
  and length(trim(player_uuid)) >= 8
  and length(trim(player_name)) >= 1
  and length(trim(character_id)) >= 1
  and difficulty_tier between 0 and 3
  and outcome = 'clear'
  and duration_seconds > 0
  and clear_time_ms > 0
  and length(trim(leaderboard_patch_key)) >= 1
  and started_at_unix > 0
  and ended_at_unix >= started_at_unix
  and is_debug = false
  and jsonb_typeof(run_summary) = 'object'
);

drop policy if exists leaderboard_runs_select_anon on public.leaderboard_runs;
create policy leaderboard_runs_select_anon
